/**
 * Country flag utilities
 * Flags stored in Vercel Blob storage
 */

export type FlagRatio = '1x1' | '3x2';

const BLOB_BASE_URL = 'https://nbxsi9emg84gcrw9.public.blob.vercel-storage.com';

/**
 * Get the URL for a country flag SVG from Vercel Blob
 * @param countryCode - ISO 3166-1 alpha-2 country code (e.g., 'US', 'GB')
 * @param ratio - Flag aspect ratio: '1x1' for square or '3x2' for rectangular
 * @returns URL to the flag SVG
 */
export function getFlagUrl(countryCode: string, ratio: FlagRatio = '3x2'): string {
  if (!countryCode) return '';

  const code = countryCode.toUpperCase();

  // Flags are stored in Vercel Blob storage
  return `${BLOB_BASE_URL}/flags/${ratio}/${code}.svg`;
}

/**
 * Get inline SVG string for a country flag
 * @param countryCode - ISO 3166-1 alpha-2 country code
 * @param ratio - Flag aspect ratio
 * @returns Promise resolving to SVG string
 */
export async function getFlagSvg(countryCode: string, ratio: FlagRatio = '3x2'): Promise<string> {
  try {
    const code = countryCode.toUpperCase();
    const response = await fetch(getFlagUrl(code, ratio));

    if (!response.ok) {
      throw new Error(`Failed to fetch flag: ${response.statusText}`);
    }

    return await response.text();
  } catch (error) {
    console.error('Error fetching flag SVG:', error);
    return '';
  }
}

